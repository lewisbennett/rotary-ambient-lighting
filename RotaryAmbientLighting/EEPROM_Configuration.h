/*
 * @brief Defines the default value for the blue component of the configured color.
 */
#define EEPROM_DEFAULT_COLOR_BLUE 0xFF

/*
 * @brief Defines the default brightness value.
 */
#define EEPROM_DEFAULT_BRIGHTNESS 0x80

/*
 * @brief Defines the default value for the green component of the configured color.
 */
#define EEPROM_DEFAULT_COLOR_GREEN 0xFF

/*
 * @brief Defines the default value for the red component of the configured color.
 */
#define EEPROM_DEFAULT_COLOR_RED 0xFF

/*
 * @brief The brightness EEPROM address.
 */
#define EEPROM_ADDRESS_BRIGHTNESS 0x01

/*
 * @brief The configured color (blue) EEPROM address.
 */
#define EEPROM_ADDRESS_COLOR_BLUE 0x04

/*
 * @brief The configured color (green) EEPROM address.
 */
#define EEPROM_ADDRESS_COLOR_GREEN 0x03

/*
 * @brief The configured color (red) EEPROM address.
 */
#define EEPROM_ADDRESS_COLOR_RED 0x02

/*
 * @brief The version EEPROM address.
 */
#define EEPROM_ADDRESS_VERSION 0x00

/*
 * @brief The EEPROM layout version.
 */
#define EEPROM_LAYOUT_VERSION 1