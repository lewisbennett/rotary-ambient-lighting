/*
 * @brief Defines the digital pin used for clock (CLK) input from the rotary encoder.
 */
#define ROTARY_ENCODER_CLK_PIN PIN_PB1

/*
 * @brief Defines the digital pin used for data (DT) input from the rotary encoder.
 */
#define ROTARY_ENCODER_DT_PIN PIN_PB0

/*
 * @brief Defines the digital pin used for switch (SW) input from the rotary encoder.
 */
#define ROTARY_ENCODER_SW_PIN PIN_PB2

/*
 * @brief Defines the time to wait for a detected press of the switch (SW) to debounce, in milliseconds.
 */
#define ROTARY_ENCODER_SW_DEBOUNCE_MILLIS 10